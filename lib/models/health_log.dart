class HealthLog {
  String dayLabel;
  String date;
  String healthStatus;
  String milkOutput;
  String vaccination;
  String heatCycle;
  String pregnancy;
  String bodyCondition;
  String symptoms;
  String treatments;
  String remarks;

  HealthLog({
    required this.dayLabel,
    required this.date,
    required this.healthStatus,
    required this.milkOutput,
    required this.vaccination,
    required this.heatCycle,
    required this.pregnancy,
    required this.bodyCondition,
    required this.symptoms,
    required this.treatments,
    required this.remarks,
  });
}
